		/// Gets or sets the product brand.
		/// </summary>
		/// <value>
		/// The product brand.
		/// </value>
		public string ProductBrand { get; set; }
		/// <summary>
		/// Gets or sets the year.
		/// </summary>
		/// <value>
		/// The year.
		/// </value>
		public int Year { get; set; }
		/// <summary>
		/// Gets or sets the description.
		/// </summary>
		/// <value>
		/// The description.
		/// </value>
		public string Description { get; set; }
	}
}
